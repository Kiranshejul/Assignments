public class VarProg {
  
	public static void main(String[] args) {

		// Var can be declared any datatype
        var p = 100;
        var q = 1.90;
        var r = 'a';
        System.out.println(p);
        System.out.println(q);
        System.out.println(r);
      
        //cannot be used as a Generic type
        
	}

}
